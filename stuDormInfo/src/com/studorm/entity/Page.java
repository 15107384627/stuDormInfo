package com.studorm.entity;

public class Page {
	public int page;
	public int pageFrist;
	public int rows;
	public Integer num;
	public int getPage() {
		return page;
	}
	public void setPage(int page) {
		this.page = page;
	}
	public int getPageFrist() {
		return pageFrist;
	}
	public void setPageFrist(int pageFrist) {
		this.pageFrist = pageFrist;
	}
	public int getRows() {
		return rows;
	}
	public void setRows(int rows) {
		this.rows = rows;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
}
